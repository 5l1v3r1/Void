#pragma once
#ifndef _V_QUATERNION_H_
#define _V_QUATERNION_H_

//----------------------------------------------------------------------------------------------------
namespace Void
{
    // Quaternion
    //----------------------------------------------------------------------------------------------------
    // q = a + bi + cj + dk
    
    // Quaternion multiplication
    //----------------------------------------------------------------------------------------------------
    // * | 1  i  j  k
    //---+-----------
    // 1 | 1  i  j  k
    // i | i -1 -k  j
    // j | j -k -1  i
    // k | k  j  i -1
    
    // VQuaternion
    //----------------------------------------------------------------------------------------------------
    struct VQuaternion
    {
    public:
        //----------------------------------------------------------------------------------------------------
        VQuaternion();
        VQuaternion(const VQuaternion& _quaternion);
        ~VQuaternion();
        
    public:
        float n, i, j, k;
    };
}

#endif
